    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->rating == null || $request->komentar == null){
            return redirect()->route('comment',['barang' => $request->slug])->with('info','Masukkan Seluruh Penilaian');
        }

        Testimoni::create([
            'barang_id' => $request->barang_id,
            'user_id' => auth()->user()->id,
            'komentar' => $request->komentar,
            'rating' => $request->rating,
        ]);
    
        return redirect()->route('detail',['barang' => $request->slug])->with('toast_success','Penilaian Berhasil Ditambahkan');
    }

